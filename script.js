var findMissing = function (list) {
  for (let i = 0; i < list.length; i++) {
    if (list[i + 2]) {
      if ((list[i + 2] + list[i]) / 2 !== list[i + 1]) {
        let x = list[i + 2] - list[i + 1];
        list.splice(i + 2, 0, list[i + 1] + x);
        return list[i + 2];
      }
    }
  }
};

